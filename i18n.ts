export const t = (lang: Language, key: string, params?: Record<string, string | number>): string => {
  let text = translations[lang][key] || key;
  if (params) {
    Object.keys(params).forEach(p => {
      text = text.replace(`{${p}}`, String(params[p]));
    });
  }
  return text;
};

// Global Currency Formatter for ka-GE
export const formatCurrency = (value: number): string => {
  return value.toLocaleString('ka-GE', { minimumFractionDigits: 2, maximumFractionDigits: 2 }) + ' ₾';
};
